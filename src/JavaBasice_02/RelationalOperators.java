package JavaBasice_02;

public class RelationalOperators {
    public static void main(String[] args) {
        int a = 100;
        int b = 300;
        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a==b);
        System.out.println(a!=b);
        if(a>b)
        {
            System.out.println("a is greater");
        }
        else
        {
            System.out.println("b is greater");
        }


    }


}
